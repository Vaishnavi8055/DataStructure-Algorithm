package SwitchCase;

public class Problem2 {
    public static void main(String[] args) {

        // int n = 2;
        char c = 'v';
        switch (c) {
            case 1:
                System.out.println("First Case");
                break;

            case 2:
                System.out.println("Second Case");
                break;

            case 'v':
                System.out.println("Third Case");
                break;

            default:
                System.out.println("Default Case");
                break;
        }
    }
}
