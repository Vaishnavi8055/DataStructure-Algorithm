import java.util.Scanner;

class pb1 {
    public static void main(String[] args) {
        
        Scanner sc = new Scanner(System.in);
        int i=1; 
    
        int n = sc.nextInt();
        System.out.println();
        while (i<=n) {
            System.out.print(i+" ");
            i = i+1;
        }
    }
}
